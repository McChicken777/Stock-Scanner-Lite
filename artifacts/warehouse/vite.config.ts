import { defineConfig } from "vite";
import react from "@vitejs/plugin-react";
import tailwindcss from "@tailwindcss/vite";
import path from "path";
import runtimeErrorOverlay from "@replit/vite-plugin-runtime-error-modal";

// In Replit dev/prod these come from artifact.toml (PORT=24594, BASE_PATH="/").
// Fall back to sane defaults so an ad-hoc `vite build` (e.g. the post-merge hook
// or a manual shell build) still works without the env being set.
const parsedPort = Number(process.env.PORT);
const port = Number.isNaN(parsedPort) || parsedPort <= 0 ? 5173 : parsedPort;
const basePath = process.env.BASE_PATH || "/";

export default defineConfig({
  base: basePath,
  plugins: [
    react(),
    tailwindcss(),
    runtimeErrorOverlay(),
    ...(process.env.NODE_ENV !== "production" &&
    process.env.REPL_ID !== undefined
      ? [
          await import("@replit/vite-plugin-cartographer").then((m) =>
            m.cartographer({
              root: path.resolve(import.meta.dirname, ".."),
            }),
          ),
          await import("@replit/vite-plugin-dev-banner").then((m) =>
            m.devBanner(),
          ),
        ]
      : []),
  ],
  resolve: {
    alias: {
      "@": path.resolve(import.meta.dirname, "src"),
      "@assets": path.resolve(import.meta.dirname, "..", "..", "attached_assets"),
    },
    dedupe: ["react", "react-dom"],
  },
  root: path.resolve(import.meta.dirname),
  build: {
    outDir: path.resolve(import.meta.dirname, "dist/public"),
    emptyOutDir: true,
    rollupOptions: {
      output: {
        // Split the big, rarely-changing libraries into a stable vendor chunk so
        // they stay cached across app deploys (per-page chunks come from React.lazy).
        manualChunks: {
          "react-vendor": ["react", "react-dom"],
          "data-vendor": ["@tanstack/react-query"],
          "ui-vendor": ["lucide-react", "date-fns"],
        },
      },
    },
  },
  server: {
    port,
    host: "0.0.0.0",
    allowedHosts: true,
    fs: {
      strict: true,
      deny: ["**/.*"],
    },
  },
  preview: {
    port,
    host: "0.0.0.0",
    allowedHosts: true,
  },
});
